# Integer
print(500)
print(type(500))

# Float
a = float()
print(a)
print('Hello world')

# String
print("Hello world")
print('''Hello world''')
print("""Hello world""")
print(type('Hello world')) # Data type

# Boolean
print(True)
print(False)
print(type(True))

# None type
print(type(None)) # Когда нужно дать информацию, но самой информации нет
a = None
print(None)